public class ExemploFor {
    public static void main(String[] args) {
        for  (int carneirinhos = 1; carneirinhos <= 20; carneirinhos ++) {
       
                 /* como ler a estrutura acima: For com variavel do tipo int chamada de carneirinho
                 * com valor inicial 1. 
                 * bloco de validação: enquanto "carneirinho" for menor ou igual a 20, é realizada a contagem
                 * Então "carneirinhos ++" soma +1 a cadda contagem
                 */
        System.out.println("Contando caneirinhos " + carneirinhos);
        }
        System.out.println("Samuelen pegou um sonhinho trans.");
    }
}
